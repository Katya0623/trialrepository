            <h3>Как стать настоящим путешественником?</h3>

            <h2>1. Поймите, что мир — это не картинки в путеводителе</h2>
            <p> Прогулки по городу с редкими перебежками за зонтиком гида — это не путешествие. Так вы
                не сумеете почувствовать истинный дух того места, в котором находитесь. Любая страна или
                город — это не набор достопримечательностей, а повседневная жизнь, улочки и местные жители.
                Не бойтесь отклоняться от традиционного маршрута, спрашивайте дорогу у прохожих и
                присматривайтесь к очарованию повседневности, порой так непохожей на нашу.
            </p>

            <h2>2. Изучите страну, в которую едете</h2>
            <p> Если вы собираетесь путешествовать по стране самостоятельно, изучите заранее все ее особенности: куда
                пойти, что посмотреть, в какие близлежащие районы можно отправиться. В настоящем путешествии вы — сами
                себе гид и экскурсовод. Не пренебрегайте информацией.
            </p>

            <h2>3. Обратите внимание на традиции</h2>
            <p> Чтобы не попасть в проблемную ситуацию, необходимо четко представлять себе что в данной стране
                допускается нормами приличий, а что категорически запрещено. Иначе можно нажить неприятностей.
                Разумеется, невозможно сразу выучить все нюансы, но в каждой стране есть свод основных правил
                уместного и вообще законного поведения, нарушение которых может повлечь за собой серьезные неприятности.
                Чем больше особенностей вы будете учитывать, тем более дружественной для вас окажется страна и ее
                жители.
            </p>

            <h2>4. Будьте осторожнее с фотографированием</h2>
            <p> Не поленитесь и попросите разрешения, чтобы сфотографировать местного жителя. В некоторых культурах
                считается, что фотография крадет часть души человека, а в некоторых странах фотографирование местных без
                их согласия и вовсе незаконно. Кроме того, многие религии запрещают фотосъемку храмов и других культовых
                сооружений, а также имеются особые правила по фотографированию изображений богов. Например, многие
                туристы делают фото, стоя спиной к статуям Будды, хотя это считается оскорбительным по отношению к
                божеству. Одним словом, если не хотите впасть в немилость у местных, лишиться фотоаппарата или получить
                проблемы с органами правопорядка, будьте аккуратным и предельно тактичным фотографом. К тому же,
                попросить разрешение сделать снимок — отличный способ познакомиться с местным жителем.
            </p>

            <h2>5. Притворитесь полиглотом</h2>
            <p> В идеале — очень здорово знать несколько языков. Это хорошо тренирует мозг и развивает память, однако,
                речь сейчас не об этом. Любому местному жителю любой страны всегда очень приятно, когда иностранец,
                пусть и неумело, со смешным акцентом, но может произнести хотя бы несколько фраз: «здравствуйте»,
                «спасибо», «пожалуйста», «как дела» — волшебные ключи к сердцу местного населения. Не поленитесь,
                выучите их на языке страны, в которую едете.
            </p>

            <h2>6. Избегайте отелей</h2>
            <p> Ни один ночлег не расскажет вам столько, сколько простое жилище обычного местного жителя. Именно здесь
                вы сможете узнать, как и чем живет этот город, эта страна и эти люди. Также это возможность
                познакомиться с настоящей (а не отшлифованной ресторанами для туристов) традиционной кухней. Не говоря
                уже об экономии.
            </p>

            <h2>7. Сделайте ваше путешествие непредсказуемым</h2>
            <p> Долой обязательную программу! Да, важно наметить основные вехи путешествия, те места, которые вы
                непременно хотите увидеть. Но прелесть самостоятельной поездки в том, что вы являетесь хозяином
                собственного времени. Захотели — подольше погуляли по музею. Или поменяли маршрут прогулки по городу.
                Или вообще спонтанно отправились на природу с новыми знакомыми, пригласившими вас на пикник. Ощутите
                вкус свободы, позвольте вашему путешествию случиться с вами и подарить вам самые настоящие приключения,
                те, о которых вы будете с упоением рассказывать, а не перечислять достопримечательности, которые вы
                посетили для галочки.
            </p>

            <h2>8. Забудьте про такси</h2>
            <p> Во-первых, это дорого. Во-вторых, это еще скучнее, чем туристический автобус — здесь вам даже
                рассказывать ничего не будут. Лучше купите проездной за горстку местных денег и катайтесь, глядя на то,
                как обычные люди, которые живут здесь, едут на работу, домой или на свидание. А еще лучше — больше
                ходите пешком. Это не только полезно, но и гораздо интереснее, чем смотреть на город через окно
                транспортного средства.
                Однако, во всем нужна мера. Порой такси является единственным безопасным способом добраться до пункта
                назначения. Не впадайте в крайности и берегите себя.
            </p>

            <h2>9. Настоящие сувениры не стоят денег</h2>
            <p> Есть прекрасная фраза: привозите из путешествия впечатления, а не вещи. Количество денежных затрат не
                влияет на качество вашего отдыха. Можно три недели прожить в дорогом отеле и ничего не увидеть, кроме
                шведского стола и километра пляжа, а можно потратить немного местной валюты и привезти столько историй,
                что хватит на три поколения вперед. Как говорил французский романист Адольф Франц, «иногда один день,
                проведенный в других местах, дает больше, чем десять лет жизни дома». Так что вперед! Навстречу новым
                открытиям!
            </p>

        </div>

